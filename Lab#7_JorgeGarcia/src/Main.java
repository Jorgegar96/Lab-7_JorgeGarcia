    public boolean IDUnico(String ID) {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cajeross.getModel();
        for (int x = 0; x < model.getSize(); x++) {
            if (((Cajero) model.getElementAt(x)).getID().equals(ID)) {
                return false;
            }
        }
        return true;
    }
